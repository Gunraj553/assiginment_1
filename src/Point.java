// This class stores the points of the triangle. I have used getter and setters for storing and fetching the data.
public class Point {
    double x;
    double y;

    public void setX(double x){
        this.x = x;
    }
    public void setY(double y){
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
